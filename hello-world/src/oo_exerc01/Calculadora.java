package oo_exerc01;

public class Calculadora {

    int soma(int valorA, int valorB){
        return valorA + valorB;
    }

    int subtrai(int valorA, int valorB){
        return valorA - valorB;
    }

    int multiplica(int valorA, int valorB){
        return valorA * valorB;
    }

    double dividi(int valorA, int valorB){
        return valorA / valorB;
    }
}
